class CalculatorModel():
    def __init__(self, controller):
        self.controller = controller

    def sum(n1: int, n2: int):
        return n1+n2
    
    def substract(n1: int, n2: int):
        return n1-n2
    
    def multiply(n1: int, n2: int):
        return n1*n2
    
    def divide(n1: int, n2: int):
        return n1/n2
    
                
